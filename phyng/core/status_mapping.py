"""Canonical status records and compatibility table."""

from __future__ import annotations

from enum import Enum

from pydantic import BaseModel, Field

from phyng.core.blocked_reasons import CanonicalBlockedReason
from phyng.core.evidence_levels import CanonicalEvidenceLevel
from phyng.core.permissions import CanonicalPermission
from phyng.core.risk_levels import CanonicalRiskLevel
from phyng.core.support_levels import CanonicalSupportLevel


class CanonicalAuditEventType(str, Enum):
    STATUS_NORMALIZED = "STATUS_NORMALIZED"
    UNKNOWN_STATUS_REVIEW_REQUIRED = "UNKNOWN_STATUS_REVIEW_REQUIRED"
    REPORT_CONTRACT_RENDERED = "REPORT_CONTRACT_RENDERED"
    COMPATIBILITY_ALIAS_USED = "COMPATIBILITY_ALIAS_USED"


class CanonicalStatusRecord(BaseModel):
    domain_status: str
    domain: str
    canonical_permission: CanonicalPermission
    blocked_reasons: list[CanonicalBlockedReason] = Field(default_factory=list)
    evidence_level: CanonicalEvidenceLevel
    support_level: CanonicalSupportLevel
    risk_level: CanonicalRiskLevel | None = None
    allowed_uses: list[str] = Field(default_factory=list)
    blocked_uses: list[str] = Field(default_factory=list)
    next_actions: list[str] = Field(default_factory=list)
    audit_event_type: CanonicalAuditEventType = CanonicalAuditEventType.STATUS_NORMALIZED
    notes: str | None = None


def _record(
    status: str,
    domain: str,
    permission: CanonicalPermission,
    reasons: list[CanonicalBlockedReason],
    evidence: CanonicalEvidenceLevel,
    support: CanonicalSupportLevel,
    risk: CanonicalRiskLevel | None = None,
    allowed: list[str] | None = None,
    blocked: list[str] | None = None,
    next_actions: list[str] | None = None,
    notes: str | None = None,
) -> CanonicalStatusRecord:
    return CanonicalStatusRecord(
        domain_status=status,
        domain=domain,
        canonical_permission=permission,
        blocked_reasons=reasons,
        evidence_level=evidence,
        support_level=support,
        risk_level=risk,
        allowed_uses=allowed or [],
        blocked_uses=blocked or [],
        next_actions=next_actions or [],
        notes=notes,
    )


